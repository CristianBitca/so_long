#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned int	i;

	i = 0;
	while (i != n)
	{
		if (*(char *)s == (char)c)
			return ((void *)s);
		s++;
		i++;
	}
	return (NULL);
}
